    private void btDevolucionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDevolucionActionPerformed
        Object selectedItem = cboPrestamo.getSelectedItem();
        
        if(selectedItem.toString().equals("-Seleccionar-")){
            JOptionPane.showMessageDialog( null , " Debe escoger una libro para poder realizar el prestamo");

            return;
        }  
        
        try {
            LocalDate fecha_devolucion = LocalDate.now();
            int preestamo_id = Integer.parseInt(selectedItem.toString());
            controlador.devolucion(preestamo_id, fecha_devolucion.toString());
            actualizarTabla(usuario.getCedula());
            actualizarComboBox(usuario.getCedula());
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btDevolucionActionPerformed

